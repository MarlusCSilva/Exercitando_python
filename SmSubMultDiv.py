"""
Mostrando os resultados de dois números inseridos e seus calculos 
matematicos básicos.
"""

numero1=int(input("Digite o primeiro número: "))
numero2=int(input("Digite o segundo número: "))

soma = numero1 + numero2
multi = numero1 * numero2  
div = numero1 / numero2
sub = numero1 - numero2

print("A Soma dos seguintes números: ", soma)
print("A Multiplicação dos seguintes números: ", multi)
print("A Divisão dos seguintes números: ", div)
print("A Subtração dos seguintes números: ", sub)